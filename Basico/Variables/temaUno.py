##* Operadores Aritmeticos
int1 = 20
int2 = 5
print(f"{int1} + {int2} = {int1 + int2}")
print(f"{int1} - {int2} = {int1 - int2}") 
print(f"{int1} * {int2} = {int1 * int2}") 
print(f"{int1} / {int2} = {int1 / int2}") 
print(f"{int1} // {int2} = {int1 // int2}") 
print(f"{int1} % {int2} = {int1 % int2}") 
print(f"{int1} ** {int2} = {int1 ** int2}")

